% =========================================================================
% 分支删除相关命令
% =========================================================================
!git push origin --delete branch_2  
% 删除远程仓库中的branch_2分支
!git branch -d branch_2             
% 删除本地已合并的branch_2分支（若未合并会提示失败）
!git branch -D branch_2             
% 强制删除本地的branch_2分支（无论是否合并）


% =========================================================================
% 分支创建与切换相关命令
% =========================================================================
!git checkout -b branch_2           
% 创建并切换到branch_2分支（等价于git branch + git checkout）
!git switch master                  
% 切换到master主分支
!git switch branch_2                
% 切换到branch_2分支
!git branch                         
% 查看本地所有分支列表（当前分支会标*）


% =========================================================================
% 代码提交与推送相关命令
% =========================================================================
!git add                            
% 开始跟踪指定文件（需手动输入文件名，不常用）
!git add .                          
% 将当前目录下所有修改、新增文件添加到暂存区
!git push                           
% 将当前分支的本地提交推送到远程对应分支（需已关联远程分支）
!git push origin branch_2           
% 将本地branch_2分支推送到远程仓库（首次推送会创建远程分支）
!git commit -a -m "1031 11:33 F放大多少倍都能跑了！"

% =========================================================================
% 日志查看相关命令
% =========================================================================
!git log -1                         
% 查看最近1条提交记录的详细信息

!git checkout .
% 撤销暂存区数据